# This code simply takes a user input and outputs it to string
# First cast it into binary format, append it to an array
# We then use a dequeue and reverse the reversed of said array
# Finally cast it back to a string and use sys.stdout.write(string) instead of print()

import sys
from collections import deque

userText = input("Enter text to be printed: ")

array_of_binary = []


def string2bits(s=''):
    return [bin(ord(x))[2:].zfill(8) for x in s]
def bits2string(b=None):
    return ''.join([chr(int(x, 2)) for x in b])

asBinary = string2bits(userText)
for i in asBinary:
    array_of_binary.append(i)

def reverse(q):
    s = len(q)
    ans = deque()
    for i in range(s):
        for j in range(s - 1):
            x = q.popleft()
            q.appendleft(x)
        ans.appendleft(q.popleft())
    return ans
 
q = deque()
 
for i in reversed(array_of_binary):
    q.append(i)

q = reverse(q)

binarray = []
while (len(q) > 0):
    binarray.append(q.popleft())

stringarray = []
for i in binarray:
    stringarray.append(i)

def youMeanItsNotFinishedYet(word):
    return(bits2string(word))

